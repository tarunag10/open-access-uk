function writeModule(path, exportName, value) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(
    path,
    `// Generated by scripts/sync-static-metadata.mjs. Do not edit by hand.\nexport const ${exportName} = ${JSON.stringify(value, null, 2)};\n`,
    'utf8'
  );
}

const publicRepositories = repositories
  .filter((repo) => toolCopy[repo.id])
  .map((repo) => ({
    ...repo,
    githubUrl: `https://github.com/${repo.repo}`,
    ...toolCopy[repo.id]
  }));

writeModule('open-access-uk-site/src/repositories.js', 'publicRepositories', publicRepositories);
writeModule('contributor-tools/maintainer-helper/src/source-records.js', 'sourceRecords', sources);

console.log(
  `Synced ${publicRepositories.length} public repositories and ${sources.length} source records.`
);
